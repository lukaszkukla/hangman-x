class Colors:
    """
    This class defines basic color pallette used in the game.
    """
    RED = '\033[91m'
    VIOLET = '\033[95m'
    BLUE = '\033[94m'
    CYAN = '\033[96m'
    GREEN = '\033[92m'
    YELLOW = '\033[93m'    
    WHITE = '\033[0m'
    BOLD = '\033[1m'
    UNDERLINE = '\033[4m'
print('\n')
print(f'{Colors.RED}RED TEXT\n')
print(f'{Colors.VIOLET}VIOLET TEXT\n')
print(f'{Colors.BLUE}BLUE TEXT\n')
print(f'{Colors.CYAN}CYAN TEXT\n')
print(f'{Colors.GREEN}GREEN TEXT\n')
print(f'{Colors.YELLOW}YELLOW TEXT\n')
print(f'{Colors.WHITE}WHITE TEXT\n')
print(f'{Colors.BOLD}BOLD TEXT\n')
print(f'{Colors.UNDERLINE}UNDERLINE TEXT\n')